<!-- Icon-Daten für JavaScript -->
<script>
var iconLibrary = <?= json_encode(array_map(fn($i) => $i['svg'], $icons), JSON_UNESCAPED_UNICODE) ?>;

function updateIconPreview(select) {
    var item = select.closest('.repeater-item');
    var preview = item.querySelector('.icon-preview');
    var customField = item.querySelector('.icon-custom-field');
    var svgField = item.querySelector('textarea[name*="iconSvg"]');
    var key = select.value;

    if (key === 'custom') {
        customField.style.display = '';
        preview.innerHTML = svgField.value || '';
    } else {
        customField.style.display = 'none';
        var svg = iconLibrary[key] || '';
        preview.innerHTML = svg;
        svgField.value = svg;
    }
}

// Beim Laden: SVG-Felder für vordefinierte Icons befüllen
document.addEventListener('DOMContentLoaded', function() {
    document.querySelectorAll('.icon-select').forEach(function(sel) {
        if (sel.value !== 'custom') {
            var item = sel.closest('.repeater-item');
            var svgField = item.querySelector('textarea[name*="iconSvg"]');
            if (svgField && !svgField.value) {
                svgField.value = iconLibrary[sel.value] || '';
            }
        }
    });
});
</script>
